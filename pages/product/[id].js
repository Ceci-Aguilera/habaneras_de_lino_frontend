const config = {
  headers: {
    "Content-Type": "application/json",
  },
};

export const getStaticPaths = async () => {

  const res = await axios.get('http://127.0.0.1:8000/store/products/', config);
  const paths = await res.data.map((product) => ({
    params: { id: product.id.toString() },
  }));

  return {
    paths,
    fallback: false,
  };

};

export const getStaticProps = async (ctx) => {

  const product_id = ctx.params?.id;
    
  const response = await axios.get(`http://127.0.0.1:8000/store/products/${product_id}/`, config);

  return {
    props: {
      product: response.data,
    },
  };
};


